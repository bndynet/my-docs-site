#!/usr/bin/env node
/**
 * docs CLI (from @bndynet/docs)
 *   init [dir]          scaffold a minimal docs.config.js (and an empty docs/ folder)
 *   dev | start         docusaurus start with the packaged config
 *   build               docusaurus build with the packaged config
 *   serve               docusaurus serve
 *   clear               docusaurus clear
 *
 * Common options (parsed here, not forwarded):
 *   --config <path>   use a specific docs.config.* file (e.g. ./docs.config.prod.js)
 *
 * Any other argument is forwarded to the Docusaurus CLI verbatim. The CLI
 * always injects the packaged shim as Docusaurus's own `--config`, so the
 * consumer never writes a docusaurus.config.js — and, by the same token,
 * cannot bypass @bndynet/docs through this CLI. To run raw Docusaurus,
 * invoke `docusaurus` directly.
 */
const { spawnSync } = require('child_process');
const path = require('path');
const fs = require('fs');

const PKG_DIR = path.resolve(__dirname, '..');
const PKG_CONFIG = path.join(PKG_DIR, 'lib', 'docusaurus.config.cjs');
const TEMPLATE_DIR = path.join(PKG_DIR, 'template');

/**
 * Docusaurus anchors plugin/preset resolution at the directory of the config
 * file passed via --config (via `createRequire(siteConfigPath)`). If we point
 * it at `packages/docs/lib/docusaurus.config.cjs`, Node walks up into the
 * monorepo root's `node_modules` and can pick up a different `@docusaurus/*`
 * version than the one installed in the consumer's siteDir — producing errors
 * like "Cannot read properties of undefined (reading 'mdxCrossCompilerCache')"
 * when a newer plugin sees a siteConfig validated by an older core.
 *
 * To avoid this, we materialize a tiny shim config **inside the siteDir** and
 * hand that path to Docusaurus. The shim just re-exports the packaged config,
 * but its location guarantees `createRequire` anchors at the siteDir, so every
 * `@docusaurus/*` import resolves from the consumer's own `node_modules`.
 */
function ensureDocsConfigShim(siteDir) {
  const cacheDir = path.join(siteDir, 'node_modules', '.cache', '@bndynet', 'docs');
  fs.mkdirSync(cacheDir, { recursive: true });
  const shimPath = path.join(cacheDir, 'docusaurus.config.cjs');
  const contents =
    '// Auto-generated by @bndynet/docs. Do not edit.\n' +
    '// Anchors Docusaurus plugin/preset resolution at the siteDir.\n' +
    'module.exports = require(' +
    JSON.stringify(PKG_CONFIG) +
    ');\n';
  try {
    const current = fs.readFileSync(shimPath, 'utf8');
    if (current === contents) return shimPath;
  } catch {}
  fs.writeFileSync(shimPath, contents);
  return shimPath;
}

/**
 * In a monorepo—or whenever a transitive dep drags in an off-version of
 * `@docusaurus/*`—npm may install two copies of `@docusaurus/theme-common`
 * (or core / theme-classic). React context is keyed by identity, so a
 * provider from copy A is invisible to a hook from copy B, and SSR dies with
 *   "Hook useColorMode is called outside the <ColorModeProvider>"
 * at hydration time. The failure is loud but the root cause (duplicate
 * packages) is not obvious from the stack trace.
 *
 * We scan shallow `node_modules` layouts (top-level + one nested level under
 * each dep's own `node_modules`) and print a targeted warning with the fix.
 * Cheap: a few readdirs. Non-fatal: we only warn and keep going.
 */
function checkDocusaurusDedup(siteDir) {
  const pkgsToCheck = [
    '@docusaurus/core',
    '@docusaurus/theme-common',
    '@docusaurus/theme-classic',
  ];
  const rootNm = path.join(siteDir, 'node_modules');
  if (!fs.existsSync(rootNm)) return;

  const readVersion = (pkgJsonPath) => {
    try {
      return JSON.parse(fs.readFileSync(pkgJsonPath, 'utf8')).version || null;
    } catch {
      return null;
    }
  };
  const readDir = (d) => {
    try {
      return fs.readdirSync(d);
    } catch {
      return [];
    }
  };
  const findNested = (pkg) => {
    const found = [];
    const top = path.join(rootNm, pkg, 'package.json');
    if (fs.existsSync(top)) found.push(top);
    const scopeOfPkg = pkg.split('/')[0];
    for (const entry of readDir(rootNm)) {
      if (entry === scopeOfPkg) continue; // already handled at top
      const entryDir = path.join(rootNm, entry);
      const candidates = entry.startsWith('@')
        ? readDir(entryDir).map((inner) => path.join(entryDir, inner, 'node_modules', pkg, 'package.json'))
        : [path.join(entryDir, 'node_modules', pkg, 'package.json')];
      for (const c of candidates) if (fs.existsSync(c)) found.push(c);
    }
    return found;
  };

  const offenders = [];
  for (const pkg of pkgsToCheck) {
    const items = findNested(pkg)
      .map((p) => ({ path: p, version: readVersion(p) }))
      .filter((i) => i.version);
    const uniq = new Set(items.map((i) => i.version));
    if (uniq.size > 1) offenders.push({ pkg, items });
  }
  if (!offenders.length) return;

  console.warn(
    '[docs] WARNING: duplicate Docusaurus packages detected — this causes SSR errors',
  );
  console.warn('        (e.g. "Hook useColorMode is called outside the <ColorModeProvider>").');
  for (const { pkg, items } of offenders) {
    console.warn(`  ${pkg}:`);
    for (const item of items) {
      console.warn(`    ${item.version}  ${path.relative(siteDir, item.path)}`);
    }
  }
  console.warn('[docs] Fix: pin a single version in your root package.json, e.g.:');
  console.warn(
    '        "overrides": { ' +
      offenders.map(({ pkg, items }) => `"${pkg}": "${items[0].version}"`).join(', ') +
      ' }',
  );
  console.warn('       then remove node_modules + lockfile and reinstall.\n');
}

// ---------------------------------------------------------------------------
// TODO(docusaurus-3.10.1): remove the entire webpackbar workaround once
// @docusaurus/bundler publishes a version that depends on webpackbar@^7.
//
//   Search this repo for `TODO(docusaurus-3.10.1)` to find every piece:
//     - checkWebpackbarCompat() below
//     - its call site in runDocusaurus()
//     - the STARTER_OVERRIDES block used by ensureStarterPackage()
//     - the "overrides.webpackbar" line in the repo-root package.json
//     - the "Troubleshooting: ProgressPlugin" section in README.md
//
// Context: Docusaurus 3.10.x pins @docusaurus/bundler -> webpackbar@^6.0.1.
// webpackbar@6 assigns its own options (name/color/reporters/reporter) to the
// `this.options` field inherited from webpack's ProgressPlugin. webpack@5.106+
// tightened ProgressPlugin schema validation (additionalProperties: false) and
// throws at compiler creation:
//   ValidationError: Invalid options object. Progress Plugin has been
//   initialized using an options object that does not match the API schema.
// webpackbar fixed this in v7.0.0. Until Docusaurus bumps its dependency we
// force webpackbar@^7 via `overrides` in the consumer's root package.json.
// Tracking: https://github.com/facebook/docusaurus/issues/11923
// ---------------------------------------------------------------------------
function checkWebpackbarCompat(siteDir) {
  const nm = path.join(siteDir, 'node_modules');
  if (!fs.existsSync(nm)) return;

  const readVersion = (p) => {
    try {
      return JSON.parse(fs.readFileSync(p, 'utf8')).version || null;
    } catch {
      return null;
    }
  };
  const webpackVer = readVersion(path.join(nm, 'webpack', 'package.json'));
  const webpackbarVer = readVersion(path.join(nm, 'webpackbar', 'package.json'));
  if (!webpackVer || !webpackbarVer) return;

  const parse = (v) => v.split('.').map((s) => parseInt(s, 10) || 0);
  const [wpMaj, wpMin] = parse(webpackVer);
  const [wbMaj] = parse(webpackbarVer);

  const webpackStrict = wpMaj > 5 || (wpMaj === 5 && wpMin >= 106);
  const webpackbarTooOld = wbMaj < 7;
  if (!(webpackStrict && webpackbarTooOld)) return;

  console.warn(
    '[docs] WARNING: incompatible webpack + webpackbar versions detected.'
  );
  console.warn(
    `        webpack@${webpackVer} enforces strict ProgressPlugin validation,`
  );
  console.warn(
    `        but webpackbar@${webpackbarVer} (< 7) passes unknown options and`
  );
  console.warn(
    '        will throw "Progress Plugin has been initialized using an options'
  );
  console.warn('        object that does not match the API schema".');
  console.warn('[docs] Fix: add this to your root package.json and reinstall:');
  console.warn('        "overrides": { "webpackbar": "^7.0.0" }');
  console.warn('        rm -rf node_modules package-lock.json && npm install');
  console.warn(
    '[docs] Tracking: https://github.com/facebook/docusaurus/issues/11923\n'
  );
}

/**
 * Webpack 5 determines a `.js` file's sourceType by walking up to the nearest
 * `package.json` and reading its `"type"` field. If the consumer's siteDir
 * package.json has `"type": "commonjs"`, webpack parses every generated
 * `.docusaurus/*.js` as CommonJS and dies with multiple copies of:
 *   "'import' and 'export' may appear only with 'sourceType: module'"
 * The failures happen in generated code far from anything the user wrote, so
 * we fail fast here with a single clear message instead of letting webpack
 * emit a fog of opaque parse errors.
 *
 * A missing `"type"` field means "auto" (webpack picks ESM from content), which
 * is what Docusaurus expects. `"type": "module"` also works. Only `"commonjs"`
 * is broken.
 */
function checkSitePackageType(siteDir) {
  const pkgPath = path.join(siteDir, 'package.json');
  let pkg;
  try {
    pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
  } catch {
    return;
  }
  if (pkg.type !== 'commonjs') return;

  const rel = path.relative(process.cwd(), pkgPath) || pkgPath;
  console.error('[docs] ERROR: your package.json has `"type": "commonjs"`.');
  console.error(
    '        Docusaurus emits generated ES modules under `.docusaurus/`.'
  );
  console.error(
    '        webpack walks up to the nearest package.json to decide sourceType'
  );
  console.error(
    '        and rejects the `import`/`export` in those files with:'
  );
  console.error(
    "          \"'import' and 'export' may appear only with 'sourceType: module'\"."
  );
  console.error('[docs] Fix: open ' + rel);
  console.error('        and remove the `"type": "commonjs"` line');
  console.error('        (or change it to `"type": "module"`), then rerun.');
  process.exit(1);
}

/**
 * Docusaurus looks for a `babel.config.js` in the siteDir and uses it to
 * transform every generated `.js` file emitted into `.docusaurus/`. If that
 * file is missing, `babel-loader` falls back to its default and treats the
 * generated ES module output as CommonJS, producing errors like:
 *   "'import' and 'export' may appear only with 'sourceType: module'".
 *
 * We auto-create a minimal one pointing at the Docusaurus preset unless the
 * user already provided their own.
 */
function ensureBabelConfig(siteDir) {
  const existing = ['babel.config.js', 'babel.config.cjs', 'babel.config.mjs', '.babelrc', '.babelrc.js']
    .map((n) => path.join(siteDir, n))
    .find((p) => fs.existsSync(p));
  if (existing) return;
  const target = path.join(siteDir, 'babel.config.js');
  fs.writeFileSync(
    target,
    "// Auto-generated by @bndynet/docs. Safe to edit or replace.\n" +
      "module.exports = {\n" +
      "  presets: [require.resolve('@docusaurus/core/lib/babel/preset')],\n" +
      "};\n"
  );
}

// TODO(docusaurus-3.10.1): drop STARTER_OVERRIDES (and the "//" note it carries)
// once @docusaurus/bundler ships with webpackbar@^7. See checkWebpackbarCompat.
const STARTER_OVERRIDES = {
  webpackbar: '^7.0.0',
};
const STARTER_OVERRIDES_NOTE =
  'TODO(docusaurus-3.10.1): remove `overrides.webpackbar` once @docusaurus/bundler depends on webpackbar@^7. ' +
  'Tracking: https://github.com/facebook/docusaurus/issues/11923';

/**
 * Seed (or patch) the consumer's `package.json` so `@bndynet/docs` works out
 * of the box. `npm overrides` only take effect in the *root* package.json of
 * the install tree, so every workaround for a transitive-dep bug (like the
 * webpackbar/webpack incompatibility) has to live there — we can't fix it
 * from inside `@bndynet/docs` alone.
 *
 * Behavior:
 *   - **No package.json yet** → write a complete starter (scripts, deps,
 *     overrides, engines). This is the "scaffold" branch.
 *   - **Existing package.json** → surgically fill in ONLY the override keys
 *     declared in `STARTER_OVERRIDES` and only when they are missing. We
 *     never overwrite an existing value (the user may have intentionally
 *     pinned a different semver range), never touch other fields, and
 *     preserve the file's detected indent + trailing newline.
 *
 * Running `docs init` is therefore idempotent: run it whenever you bump
 * `@bndynet/docs` and any new managed overrides get filled in automatically.
 * When an upstream bug gets fixed, the maintainer drops the entry from
 * `STARTER_OVERRIDES` (and the related TODO-marked code), republishes, and
 * downstream consumers remove the now-stale override by hand.
 */
function ensureStarterPackage(siteDir) {
  const target = path.join(siteDir, 'package.json');
  if (fs.existsSync(target)) {
    return mergeStarterOverrides(target);
  }

  let docsVersion = '0.0.0';
  try {
    docsVersion = require(path.join(PKG_DIR, 'package.json')).version || docsVersion;
  } catch {}

  const name = (path.basename(siteDir) || 'my-docs-site')
    .toLowerCase()
    .replace(/[^a-z0-9._~-]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'my-docs-site';

  const pkg = {
    name,
    version: '0.0.0',
    private: true,
    '//': STARTER_OVERRIDES_NOTE,
    scripts: {
      start: 'docs dev',
      dev: 'docs dev',
      build: 'docs build',
      serve: 'docs serve',
      clear: 'docs clear',
    },
    dependencies: {
      '@bndynet/docs': `^${docsVersion}`,
    },
    overrides: { ...STARTER_OVERRIDES },
    engines: {
      node: '>=20.0.0',
    },
  };
  fs.writeFileSync(target, JSON.stringify(pkg, null, 2) + '\n');
  console.log(`created ${path.relative(process.cwd(), target) || target}`);
}

/**
 * Idempotent merge: fill in any missing override key from `STARTER_OVERRIDES`
 * into an existing `package.json`, preserving the file's indentation and
 * trailing newline. Existing override values are *never* overwritten — if the
 * consumer intentionally pinned a different range, `checkWebpackbarCompat`
 * (and its siblings) will surface a runtime warning; we don't second-guess
 * user intent silently.
 *
 * Returns nothing; logs a per-run summary to stdout.
 */
function mergeStarterOverrides(target) {
  const rel = path.relative(process.cwd(), target) || target;
  let raw;
  try {
    raw = fs.readFileSync(target, 'utf8');
  } catch (err) {
    console.warn(`skip (read failed): ${rel} — ${err.message}`);
    return;
  }
  let pkg;
  try {
    pkg = JSON.parse(raw);
  } catch (err) {
    console.warn(`skip (not valid JSON): ${rel} — ${err.message}`);
    console.warn('  fix the JSON and re-run `docs init` to sync overrides.');
    return;
  }

  const overrides = pkg.overrides && typeof pkg.overrides === 'object' ? pkg.overrides : {};
  const added = [];
  for (const [key, val] of Object.entries(STARTER_OVERRIDES)) {
    // Only fill when missing; respect any value the user has already set.
    if (overrides[key] === undefined) {
      overrides[key] = val;
      added.push(`${key}: ${val}`);
    }
  }
  if (!added.length) {
    console.log(`skip (overrides already in place): ${rel}`);
    return;
  }
  pkg.overrides = overrides;

  // Preserve detected indent (tabs or spaces) and trailing newline.
  const indentMatch = raw.match(/\n([ \t]+)"/);
  const indent = indentMatch ? indentMatch[1] : '  ';
  const trailing = raw.endsWith('\n') ? '\n' : '';
  fs.writeFileSync(target, JSON.stringify(pkg, null, indent) + trailing);

  console.log(`updated ${rel} — added ${added.length} override(s):`);
  for (const line of added) console.log(`  + ${line}`);
  console.log(
    '  then: rm -rf node_modules package-lock.json && npm install'
  );
}

const args = process.argv.slice(2);
const cmd = args[0] || 'dev';
const rest = args.slice(1);

function run() {
  if (cmd === 'init') return runInit(rest);
  if (cmd === '--help' || cmd === '-h' || cmd === 'help') return printHelp(0);
  return runDocusaurus(cmd, rest);
}

function runInit(argv) {
  const target = path.resolve(process.cwd(), argv[0] || '.');
  fs.mkdirSync(target, { recursive: true });

  // Seed a minimal docs.config.js if none exists.
  const existing = ['docs.config.ts', 'docs.config.js', 'docs.config.cjs', 'docs.config.mjs']
    .map((n) => path.join(target, n))
    .find((p) => fs.existsSync(p));

  if (existing) {
    console.log(`skip (exists): ${path.relative(process.cwd(), existing) || existing}`);
  } else {
    const src = path.join(TEMPLATE_DIR, 'docs.config.js');
    const dst = path.join(target, 'docs.config.js');
    fs.copyFileSync(src, dst);
    console.log(`created ${path.relative(process.cwd(), dst) || dst}`);
  }

  const docsDir = path.join(target, 'docs');
  if (!fs.existsSync(docsDir)) {
    fs.mkdirSync(docsDir, { recursive: true });
    fs.writeFileSync(
      path.join(docsDir, 'intro.md'),
      '# Welcome\n\nThis is your first doc. Edit `docs/intro.md`.\n'
    );
    console.log(`created ${path.relative(process.cwd(), docsDir) || docsDir}/intro.md`);
  }

  // Seed a minimal package.json so `npm install` works out of the box and so
  // we can land the webpackbar `overrides` where npm actually honours them
  // (root of the install tree).
  ensureStarterPackage(target);

  const rel = path.relative(process.cwd(), target);
  const cd = !rel || rel === '.' ? '.' : rel;
  console.log(`\nNext: cd ${cd} && npm install && npx docs dev`);
  process.exit(0);
}

function runDocusaurus(command, forwardArgs) {
  const siteDir = process.cwd();
  const docusaurusBin = resolveDocusaurusBin(siteDir);
  if (!docusaurusBin) {
    console.error(
      '[docs] Cannot find @docusaurus/core. Install it (and @docusaurus/preset-classic) in your project.'
    );
    process.exit(1);
  }

  // Consume `--config <path>` (or `--config=<path>`, `-c <path>`) from argv so
  // Docusaurus never sees it — for this CLI, `--config` means the user's
  // docs.config.*, not a Docusaurus config. Exposed via env for
  // lib/docusaurus.config.cjs (and lib/sidebars.cjs).
  const { docsConfig, rest: cleanArgs } = extractDocsConfig(forwardArgs);

  const docusaurusCmd = command === 'dev' ? 'start' : command;
  checkSitePackageType(siteDir);
  ensureBabelConfig(siteDir);

  // Warn early on duplicate Docusaurus packages for commands that actually
  // render React (dev/start/build). `serve`/`clear` don't need it.
  if (docusaurusCmd === 'start' || docusaurusCmd === 'build') {
    checkDocusaurusDedup(siteDir);
    // TODO(docusaurus-3.10.1): remove this call when the workaround is no
    // longer needed — see checkWebpackbarCompat().
    checkWebpackbarCompat(siteDir);
  }

  // Always inject the packaged shim as Docusaurus's --config. The shim loads
  // the docs.config that extractDocsConfig resolved (via DOCS_CONFIG) or
  // auto-discovers one in siteDir.
  const shimPath = ensureDocsConfigShim(siteDir);
  const forwarded = [docusaurusCmd, '--config', shimPath, ...cleanArgs];

  const env = { ...process.env, DOCS_DIR: siteDir };
  if (docsConfig) {
    env.DOCS_CONFIG = path.isAbsolute(docsConfig)
      ? docsConfig
      : path.resolve(siteDir, docsConfig);
  }

  const result = spawnSync(process.execPath, [docusaurusBin, ...forwarded], {
    stdio: 'inherit',
    cwd: siteDir,
    env,
  });
  process.exit(result.status ?? 1);
}

/**
 * Pull the docs-config flag (`--config <path>`, `--config=<path>`, or `-c <path>`)
 * out of argv. In this CLI `--config` refers to the user's docs.config.*, not a
 * Docusaurus config — so we consume it here and it never reaches Docusaurus.
 * Last occurrence wins (matches `yargs`/most CLI conventions).
 */
function extractDocsConfig(argv) {
  const rest = [];
  let docsConfig = null;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--config' || a === '-c') {
      docsConfig = argv[++i];
      continue;
    }
    if (typeof a === 'string' && a.startsWith('--config=')) {
      docsConfig = a.slice('--config='.length);
      continue;
    }
    rest.push(a);
  }
  return { docsConfig, rest };
}

function resolveDocusaurusBin(fromDir) {
  const tryResolve = (subpath) => {
    try {
      return require.resolve(subpath, { paths: [fromDir, PKG_DIR] });
    } catch {
      return null;
    }
  };
  return (
    tryResolve('@docusaurus/core/bin/docusaurus.mjs') ||
    tryResolve('@docusaurus/core/bin/docusaurus.js')
  );
}

function printHelp(code) {
  console.log(
    [
      'Usage: docs <command> [options]',
      '',
      'Commands:',
      '  init [dir]   Scaffold a minimal docs.config.js (+ docs/intro.md)',
      '  dev|start    Run the Docusaurus dev server',
      '  build        Build the site for production',
      '  serve        Serve a production build',
      '  clear        Clear the Docusaurus cache',
      '',
      'Options:',
      '  --config <path>   Use a specific docs.config file (e.g. ./docs.config.prod.js)',
      '',
      'All commands (except init) forward to @docusaurus/core with a siteDir-anchored',
      'shim config that re-exports:',
      '  ' + PKG_CONFIG,
      'and DOCS_DIR set to the current working directory.',
    ].join('\n')
  );
  process.exit(code);
}

run();
